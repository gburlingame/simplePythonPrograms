num = int(input("Enter the number:"))
if(num>=0) and (num<=10):
    print("The range of the number is 0-9.")
elif(num>=11) and (num<=20):
    print("The range of the number is 11-20.")
elif(num>=21) and (num<=30):
    print("The range of the number is 21-30.")
elif(num>=31) and (num<=40):
    print("The range of the number is 31-40.")
else: 
    print("Number is bigger than 40.")