day=int(input("enter day no.:"))
if (day == 1):
    print("MONDAY")
elif (day == 2):
    print("TUESDAY")
elif (day == 3):
    print("WEDNESDAY")
elif (day == 4):
    print("THURSDAY")
elif (day == 5):
    print("FRIDAY")
elif (day == 6):
    print("SATURDAY")
elif (day == 7):
    print("SUNDAY")
else: print("day either exceeds number 7 or is invalid")

